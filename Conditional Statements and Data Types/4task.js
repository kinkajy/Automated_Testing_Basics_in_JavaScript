//Perform explicit conversion 


//number
let str = "5";
let typestr = typeof str;
console.log ("str =", typestr)
let num = Number(str)
let typenum = typeof num;
console.log("num =", typenum);

//string
let value = true;
let typevalue = typeof value;
console.log ("value =", typevalue)
let string = String(value)
let typestring = typeof string;
console.log("string =", typestring);

//boolean
let st = "true";
let typest = typeof st;
console.log ("st =", typest)
let bool = Boolean(st)
let typebool = typeof bool;
console.log("bool =", typebool);